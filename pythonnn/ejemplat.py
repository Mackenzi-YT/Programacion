# Solicitar al usuario que ingrese cuatro números
num1 = int(input("Ingrese el primer número: "))
num2 = int(input("Ingrese el segundo número: "))
num3 = int(input("Ingrese el tercer número: "))
num4 = int(input("Ingrese el cuarto número: "))

# Realizar la suma de los dos primeros números
suma = num1 + num2

# Realizar la multiplicación de los dos últimos números
producto = num3 * num4

# Mostrar los resultados
print("La suma de los dos primeros números es:", suma)
print("El producto de los dos últimos números es:", producto)